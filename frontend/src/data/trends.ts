// Build the full trends map
export function getBrandTrends(brandId: string): TrendCard[] {
	const sector = BRAND_SECTOR[brandId];
	if (!sector) return [];

	const companyTrend = COMPANY_TRENDS[brandId];
	if (!companyTrend) return [];

	const stakInsight = STAK_INSIGHTS[brandId];
	if (!stakInsight) return [];

	return [
		{ type: "macro", ...MACRO_TRENDS[sector] },
		{ type: "sector", ...SECTOR_TRENDS[sector] },
		{ type: "company", ...companyTrend },
		{ type: "stak", ...stakInsight },
	];
}
